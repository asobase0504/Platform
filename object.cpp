#include "object.h"
#include "application.h"
#include <assert.h>

//=============================================================================
// コンストラクタ
//=============================================================================
CObject::CObject(int inPriority, CTaskGroup::EPushMethod inMethod) :
	CTask(inPriority, inMethod),
	m_updateStatus(POP)
{
}

//=============================================================================
// デストラクタ
//=============================================================================
CObject::~CObject()
{
}

//=============================================================================
// 更新
//=============================================================================
void CObject::Update()
{
	switch (m_updateStatus)
	{
	case CObject::POP:
		PopUpdate();
		break;
	case CObject::NORMAL:
		NormalUpdate();
		break;
	case CObject::END:
		EndUpdate();
		break;
	default:
		assert(false);
		break;
	}
}

//=============================================================================
// 出現中の更新
//=============================================================================
void CObject::PopUpdate()
{
	m_updateStatus = NORMAL;
}

//=============================================================================
// 通常中の更新
//=============================================================================
void CObject::NormalUpdate()
{
}

//=============================================================================
// 終了中の更新
//=============================================================================
void CObject::EndUpdate()
{
	Release();
}
